package week1.assignment;

public class FibonacciSeries {

	public static void main(String[] args) {
		int num=8;
		int firstNum=0;
		int SecondNum=1;
		for(int i=0;i<num;i++)
		{
			System.out.println(firstNum);
			int nextNum=firstNum+SecondNum;
			firstNum=SecondNum;
			SecondNum=nextNum;
			
	
			
			
		}
	}

}
